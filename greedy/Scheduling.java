import java.io.*;
import java.util.*;

class Scheduling{

	public static void main(String[] args) {
		int T, N;
		Scanner in = new Scanner(System.in);
		T = in.nextInt();
		for(int i = 0; i < T; i++){
			N = in.nextInt();
			int T[] = new int[N];
			int P[] = new int[N];
			for(int j = 0; j < N; j++){
				T[i] = in.nextInt();
				P[i] = in.nextInt();
			}
			
		}
	}
}